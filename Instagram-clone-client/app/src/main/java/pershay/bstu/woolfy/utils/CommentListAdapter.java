import de.hdodenhof.circleimageview.CircleImageView;
import pershay.bstu.woolfy.R;
import pershay.bstu.woolfy.models.Comment;

public class CommentListAdapter extends ArrayAdapter<Comment> {

    private static final String TAG = "CommentListAdapter";

    private LayoutInflater mInflater;
    private int layoutResource;
    private Context mContext;

    public CommentListAdapter(@NonNull Context context, @LayoutRes int resource,
                              @NonNull List<Comment> objects) {
        super(context, resource, objects);
        mInflater = (LayoutInflater) context.getSystemService(Context.LAYOUT_INFLATER_SERVICE);
        mContext = context;
        layoutResource = resource;
    }

    private static class ViewHolder{
        TextView comment;
        CircleImageView profileImage;
        ImageView like;
    }

    @NonNull
    @Override
    public View getView(final int position, @Nullable View convertView, @NonNull ViewGroup parent) {

        final ViewHolder holder;

        if(convertView == null){
            convertView = mInflater.inflate(layoutResource, parent, false);
            holder = new ViewHolder();

            holder.comment = (TextView) convertView.findViewById(R.id.comment);
            holder.profileImage = convertView.findViewById(R.id.comment_profile_image);
            String imageUrl = "sun9-55.userapi.com/c851532/v851532687/14e4c6/s7yxMOGXjS4.jpg";
            ImageLoader.getInstance().init(ImageLoaderConfiguration.createDefault(getContext()));
            UniversalImageLoader.setImage(imageUrl, holder.profileImage, null, "https://");

            convertView.setTag(holder);
        }else{
            holder = (ViewHolder) convertView.getTag();
        }

        holder.comment.setText(getItem(position).getComment());

        try{
            if(position == 0){
                holder.like.setVisibility(View.GONE);
            }
        }catch (NullPointerException e){
            Log.e(TAG, "getView: NullPointerException: " + e.getMessage() );
        }

        return convertView;
    }
}

